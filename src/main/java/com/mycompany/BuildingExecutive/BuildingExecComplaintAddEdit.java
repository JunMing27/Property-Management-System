    private String addEditDetector;
    
    public void addEditDetect(String functionType,String id) throws FileNotFoundException{
        if(functionType=="edit"){
            this.addEditDetector="edit";
            BuildingExecMain main = new BuildingExecMain();
            main.getDataViewSingle(id, "Complaint.txt","complaint");
            main.getDropDownData("ResidentProfile.txt","complaint");
            UpdateAddBtn.setText("Update");
            ComplaintIDTxt.setText(main.getComplaintID());
            ComplaintDetailsTxt.setText(main.getComplaintDetails());
            ComplaintReplyTxt.setText(main.getComplaintReply());
            int i=0;
            for (String x : main.getDropDownData()) {
                jComboBox6.addItem((main.getDropDownData()).get(i));
                i++;
            }
            jComboBox6.setSelectedItem(main.getComplaintUserId());
        }else if (functionType=="add"){
            this.addEditDetector="add";
            BuildingExecMain main = new BuildingExecMain();
            UpdateAddBtn.setText("Add");
            UpdateAddBtn.setVisible(false);ComplaintIDTxt.setVisible(false);
            jLabel28.setVisible(false);jLabel31.setVisible(false);
            main.getDropDownData("ResidentProfile.txt","complaint");
            int i=0;
            for (String x : main.getDropDownData()) {
                jComboBox6.addItem((main.getDropDownData()).get(i));
                i++;
            }
        }
    }
    
