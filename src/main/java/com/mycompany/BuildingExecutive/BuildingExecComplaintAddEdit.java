    private void UpdateAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateAddBtnActionPerformed
        // TODO add your handling code here:
        if(addEditDetector=="edit"){
            if((ComplaintDetailsTxt.getText()).equals("") || (ComplaintReplyTxt.getText()).equals("") ||((String)jComboBox6.getSelectedItem()).equals("") ){
                JOptionPane.showMessageDialog(null, "Enter all field", "Warning", JOptionPane.ERROR_MESSAGE);
            }else{
                BuildingExecMain main = new BuildingExecMain();
                ArrayList<String> dataList = new ArrayList<String>();
                dataList.add(ComplaintIDTxt.getText());
                dataList.add(ComplaintDetailsTxt.getText());
                dataList.add(ComplaintReplyTxt.getText());
                dataList.add((String)jComboBox6.getSelectedItem());
                main.editOrAddData(dataList, "complaint","Complaint.txt","edit");
            }
        }else if(addEditDetector=="add"){
            if((ComplaintReplyTxt.getText()).equals("") ||((String)jComboBox6.getSelectedItem()).equals("") ){
                JOptionPane.showMessageDialog(null, "Enter all field", "Warning", JOptionPane.ERROR_MESSAGE);
            }else{
                BuildingExecMain main = new BuildingExecMain();
                ArrayList<String> dataList = new ArrayList<String>();
                main.getIncreasedID("Complaint.txt","complaint");
                dataList.add(main.getComplaintID());
                dataList.add(ComplaintDetailsTxt.getText());
                dataList.add("-");
                dataList.add((String)jComboBox6.getSelectedItem());
                main.editOrAddData(dataList, "complaint","Complaint.txt","add");
                ComplaintIDTxt.setText("");
                ComplaintDetailsTxt.setText("");
            }
        }
    }//GEN-LAST:event_UpdateAddBtnActionPerformed

