    public void setComplaintData(){
        BuildingExecMain main = new BuildingExecMain();
        main.chooseTxtFile("Complaint");
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"complaint");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextButton.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getComplaintID() !=null ){
                ComplaintID.setText(main.getComplaintID());
                ComplaintDetails.setText(main.getComplaintDetails());
                ComplaintReply.setText(main.getComplaintReply());
            }else{
                ComplaintID.setText("no data");
                ComplaintDetails.setText("no data");
                ComplaintReply.setText("no data");
            }
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"complaint");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextButton.setEnabled(false);
                    System.out.println("false");
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getComplaintID() !=null ){
                ComplaintID1.setText(main.getComplaintID());
                ComplaintDetails1.setText(main.getComplaintDetails());
                ComplaintReply1.setText(main.getComplaintReply());
            }else{
                ComplaintID1.setText("no data");
                ComplaintDetails1.setText("no data");
                ComplaintReply1.setText("no data");
            }
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"complaint");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextButton.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getComplaintID() !=null ){
                ComplaintID2.setText(main.getComplaintID());
                ComplaintDetails2.setText(main.getComplaintDetails());
                ComplaintReply2.setText(main.getComplaintReply());
            }else{
                ComplaintID2.setText("no data");
                ComplaintDetails2.setText("no data");
                ComplaintReply2.setText("no data");
            }
    }
    
