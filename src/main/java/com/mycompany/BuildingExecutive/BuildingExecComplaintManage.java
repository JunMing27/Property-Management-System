    private void newComplaint(String line) {
        ModelComplaint complaint = new ModelComplaint(line);
        JPanel complaintPanel = new JPanel();

        complaintPanel.setBorder(BorderFactory.createCompoundBorder(BorderFactory.createLineBorder(Color.black), new EmptyBorder(0,40,0,0)));
        // complaintPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        // complaintPanel.setBorder();
        complaintPanel.setPreferredSize(new Dimension(300, 180));
        complaintPanel.setLayout(new GridBagLayout());

        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = 0;
        gbc.gridy = 1;
        gbc.anchor = GridBagConstraints.WEST;
        gbc.weightx = 1.0;
        gbc.weighty = 1.0;


        complaintPanel.add(new JLabel("Complaint ID: "), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel("Complaint Details: "), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel("Complaint Reply: "), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel("Resident ID: "), gbc);

        gbc.gridx = 1;
        gbc.gridy = 1;
        complaintPanel.add(new JLabel(complaint.getComplaintId()), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel(complaint.getComplaintDetails()), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel(complaint.getComplaintReply()), gbc);
        gbc.gridy++;
        complaintPanel.add(new JLabel(complaint.getResidentId()), gbc);

        JButton viewBtn = new JButton("View Details");
        viewBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                changeFrame(complaint);
            }
        });

        JButton removeBtn = new JButton("Remove Details");
        removeBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {

                String fileName = "./com/mycompany/textFile/Complaint.txt";
                String lineToRemove = complaint.toString();

                try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                    StringBuilder sb = new StringBuilder();
                    String line;
                    while ((line = br.readLine()) != null) {
                        if (!line.trim().equals(lineToRemove)) {
                            sb.append(line).append("\n");
                        }
                    }
                    try (BufferedWriter bw = new BufferedWriter(new FileWriter(fileName))) {
                        bw.write(sb.toString());
                    } catch (IOException e) {
                        System.err.println("Error writing to file: " + e.getMessage());
                    }
                } catch (IOException e) {
                    System.err.println("Error reading file: " + e.getMessage());
                }

                System.out.println("REMOVED! "+complaint);
                DataPanel.remove(complaintPanel);
                invalidate();
                validate();
                repaint();
            }
        });

        gbc.gridx = 2;
        gbc.gridy = 1;
        complaintPanel.add(viewBtn, gbc);
        gbc.gridy = 2;
        complaintPanel.add(removeBtn, gbc);

        DataPanel.add(complaintPanel, BorderLayout.CENTER);
    }
    
