    private void DeleteAccExecutiveInoviceViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteAccExecutiveInoviceViewActionPerformed
        // TODO add your handling code here:
        BuildingExecMain main = new BuildingExecMain();
        main.chooseTxtFile("EmployeeJob");
        main.deleteFunction(CheckRecID.getText());
        JOptionPane.showMessageDialog(null, "Deleted Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        BuildingExecPatrolManage Job = new BuildingExecPatrolManage();
        Job.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        Job.pack();
        Job.setResizable(false);
        Job.setLocationRelativeTo(null);
        Job.setVisible(true);
        Job.backButtonToggle();// need to check back with manage
        Job.setPatrolData(); //same as above
    }//GEN-LAST:event_DeleteAccExecutiveInoviceViewActionPerformed

    public void setDataViewSingle(String id) throws FileNotFoundException{
        BuildingExecMain main = new BuildingExecMain();
        main.getDataViewSingle(id, "InvoiceContent.txt","Invoice");
        //after getting data, set text value 
        CheckRecID.setText(main.getCheckpointRecordID());
        UserID.setText(main.getUserID());
        CheckpointDate.setText(main.getCheckpointDate());
        BlockNum.setText(main.getBlockNumber());
        
    }
