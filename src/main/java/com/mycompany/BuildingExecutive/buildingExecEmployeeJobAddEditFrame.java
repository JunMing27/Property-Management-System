    private String addEditDetector;
    private String fileType="EmployeeJob.txt";
    private String userType="EmployeeJob";
    
    public void addEditDetect(String functionType,String id) throws FileNotFoundException {
        if(functionType=="edit"){
            this.addEditDetector="edit";
            employeeJob employeeJobClass = new employeeJob();
            employeeJobClass.chooseTxtFile("EmployeeJob");
            employeeJobClass.getDataViewSingle(id, fileType,userType);
            employeeJob.employeeJobMethod mainInner = employeeJobClass.new employeeJobMethod();
            
            addEditBtn.setText("Update");
            jobIdField.setText(employeeJobClass.getJobId());
            jobField.setText(employeeJobClass.getEmployeeJob());
            jobDescField.setText(employeeJobClass.getEmployeeJobDescription());
            employeeField.setText(employeeJobClass.getEmployeeId());
        }else if (functionType=="add"){
            this.addEditDetector="add";
            addEditBtn.setText("Add");
            jobIdField.setVisible(false);
            jobIdLabel.setVisible(false);
        }
    }
    
