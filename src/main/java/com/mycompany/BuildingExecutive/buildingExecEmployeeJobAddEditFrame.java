    private void addEditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEditBtnActionPerformed
        // TODO add your handling code here:
        if(addEditDetector=="edit"){
                buildingExecutive main = new buildingExecutive();
                buildingExecutive.buildingExecutiveMethod mainInner = main.new buildingExecutiveMethod();
                ArrayList<String> dataList = new ArrayList<String>();
                dataList.add(jobIdField.getText());
                dataList.add(jobField.getText());
                dataList.add(jobDescField.getText());
                dataList.add(employeeField.getText());
                mainInner.addEditEmployeeJob(dataList, "EmployeeJob","EmployeeJob.txt","edit");
        }else if(addEditDetector=="add"){
                
                buildingExecutive main = new buildingExecutive();
                buildingExecutive.buildingExecutiveMethod mainInner = main.new buildingExecutiveMethod();
                employeeJob employeeJobClass = new employeeJob();
                ArrayList<String> dataList = new ArrayList<String>();
                employeeJobClass.getIncreasedID("EmployeeJob.txt","EmployeeJob");
                dataList.add(employeeJobClass.getJobId());
                dataList.add(jobField.getText());
                dataList.add(jobDescField.getText());
                dataList.add(employeeField.getText());

                mainInner.addEditEmployeeJob(dataList, "EmployeeJob","EmployeeJob.txt","add");
        }
    }//GEN-LAST:event_addEditBtnActionPerformed

