    public void backButtonFunction(){
        PageLine = PageLine -6;
        if (PageLine==-1){
            backPageBtn.setEnabled(false);
        }
    }
    
    private String searchTxt ="";

    private Integer PageLine=-1;
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    
   public void setInvoiceData(){
        invoice invoiceClass = new invoice();
        setPagination();
        invoiceClass.displayDataView(PageLine,searchTxt,"Invoice");
        boolean boo = invoiceClass.getStatus();
        if(boo==false){
            nextPageBtn.setEnabled(false);
        }
        System.out.println(invoiceClass.getInvoiceId());
        if(invoiceClass.getInvoiceId() !=null){
            invoiceIdTxt.setText(invoiceClass.getInvoiceId());
            userIdTxt.setText(invoiceClass.getUserId());
            tenantNameTxt.setText(invoiceClass.getUserName());
            dateIssuedTxt.setText(invoiceClass.getIssuedDate());
            dueDateTxt.setText(invoiceClass.getDueDate());
            dueAmountTxt.setText(invoiceClass.getDueAmount());
            totalAmountTxt.setText(invoiceClass.getTotalAmount());
            descTxt.setText(invoiceClass.getPaymentDesc());

        }else{
            invoiceIdTxt.setText("no data");
            userIdTxt.setText("no data");
            tenantNameTxt.setText("no data");
            dateIssuedTxt.setText("no data");
            dueDateTxt.setText("no data");
            dueAmountTxt.setText("no data");
            totalAmountTxt.setText("no data");
            descTxt.setText("no data");
        }

    }
