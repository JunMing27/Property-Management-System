    private void newStatement(String line) {
        ModelStatement statement = new ModelStatement(line);
        JPanel statementPanel = new JPanel();

        statementPanel.setBorder(BorderFactory.createCompoundBorder(BorderFactory.createLineBorder(Color.black), new EmptyBorder(0,40,0,0)));
        // statementPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        // statementPanel.setBorder();
        statementPanel.setPreferredSize(new Dimension(300, 180));
        statementPanel.setLayout(new GridBagLayout());

        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = 0;
        gbc.gridy = 1;
        gbc.anchor = GridBagConstraints.WEST;
        gbc.weightx = 1.0;
        gbc.weighty = 1.0;


        statementPanel.add(new JLabel("Statement ID: "), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel("User ID: "), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel("Username: "), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel("Paid Date: "), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel("Paid Amount: "), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel("Description: "), gbc);

        gbc.gridx = 1;
        gbc.gridy = 1;
        statementPanel.add(new JLabel(statement.getStatementId()), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel(statement.getUserId()), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel(statement.getUsername()), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel(statement.getPaidDate()), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel(statement.getPaidAmount()), gbc);
        gbc.gridy++;
        statementPanel.add(new JLabel(statement.getDescription()), gbc);

        JButton viewBtn = new JButton("View Details");
        viewBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                changeFrame(statement);
            }
        });

        JButton removeBtn = new JButton("Remove Details");
        removeBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {

                String fileName = "./com/mycompany/textFile/ReceiptContent.txt";
                String lineToRemove = statement.toString();

                try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                    StringBuilder sb = new StringBuilder();
                    String line;
                    while ((line = br.readLine()) != null) {
                        if (!line.trim().equals(lineToRemove)) {
                            sb.append(line).append("\n");
                        }
                    }
                    try (BufferedWriter bw = new BufferedWriter(new FileWriter(fileName))) {
                        bw.write(sb.toString());
                    } catch (IOException e) {
                        System.err.println("Error writing to file: " + e.getMessage());
                    }
                } catch (IOException e) {
                    System.err.println("Error reading file: " + e.getMessage());
                }

                System.out.println("REMOVED! "+statement);
                DataPanel.remove(statementPanel);
                invalidate();
                validate();
                repaint();
            }
        });

        gbc.gridx = 2;
        gbc.gridy = 1;
        statementPanel.add(viewBtn, gbc);
        gbc.gridy = 2;
        statementPanel.add(removeBtn, gbc);

        DataPanel.add(statementPanel, BorderLayout.CENTER);
    }
    
