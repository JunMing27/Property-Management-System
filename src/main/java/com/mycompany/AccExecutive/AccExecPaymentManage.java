    private Integer PageLine=-1;
    
    private String searchTxt ="";
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    public void setPaymentData(){
        AccExecMain main = new AccExecMain();
        main.chooseTxtFile("InvoiceContent");
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Invoice");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecPaymentManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            ResidentID1.setText(main.getInvoiceNumber());
            PaymentReceiver1.setText(main.getTenantName());//need to add at mainclass
            Amount1.setText(main.getDateInvoiceIssued());//need to add at mainclass
            DueDate1.setText(main.getDateDue());//need to add at mainclass
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Payment");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecPaymentManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getInvoiceNumber() !=null){
                ResidentID2.setText(main.getInvoiceNumber());
                PaymentReceiver2.setText(main.getTenantName());//need to add at mainclass
                Amount2.setText(main.getDateInvoiceIssued());//need to add at mainclass
                DueDate2.setText(main.getDateDue());//need to add at mainclass
            }else{
                ResidentID2.setText("no data");
                PaymentReceiver2.setText("no data");
                Amount2.setText("no data");
                DueDate2.setText("no data");
                
            }
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Invoice");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecPaymentManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getInvoiceNumber() !=null){
                ResidentID3.setText(main.getInvoiceNumber());
                PaymentReceiver3.setText(main.getTenantName());//need to add at mainclass
                Amount3.setText(main.getDateInvoiceIssued());//need to add at mainclass
                DueDate3.setText(main.getDateDue());//need to add at mainclass
                
            }else{
                ResidentID3.setText("no data");
                PaymentReceiver3.setText("no data");
                Amount3.setText("no data");
                DueDate3.setText("no data");
                
            }
    }
    
    
