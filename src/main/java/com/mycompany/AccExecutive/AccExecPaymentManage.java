    private void newPayment(String line) {
        ModelPayment payment = new ModelPayment(line);
        JPanel paymentPanel = new JPanel();

        paymentPanel.setBorder(BorderFactory.createCompoundBorder(BorderFactory.createLineBorder(Color.black), new EmptyBorder(0,40,0,0)));
        // paymentPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        // paymentPanel.setBorder();
        paymentPanel.setPreferredSize(new Dimension(300, 180));
        paymentPanel.setLayout(new GridBagLayout());

        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = 0;
        gbc.gridy = 1;
        gbc.anchor = GridBagConstraints.WEST;
        gbc.weightx = 1.0;
        gbc.weighty = 1.0;


        paymentPanel.add(new JLabel("Payment ID: "), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel("User ID: "), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel("Payment Description: "), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel("Payment Amount: "), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel("Payment Due Date: "), gbc);

        gbc.gridx = 1;
        gbc.gridy = 1;
        paymentPanel.add(new JLabel(payment.getPaymentId()), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel(payment.getUserId()), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel(payment.getPayDescription()), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel(payment.getPayAmount()), gbc);
        gbc.gridy++;
        paymentPanel.add(new JLabel(payment.getDueDate()), gbc);

        JButton viewBtn = new JButton("View Details");
        viewBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                changeFrame(payment);
            }
        });

        JButton removeBtn = new JButton("Remove Details");
        removeBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {

                String fileName = "./com/mycompany/textFile/Payment.txt";
                String lineToRemove = payment.toString();

                try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                    StringBuilder sb = new StringBuilder();
                    String line;
                    while ((line = br.readLine()) != null) {
                        if (!line.trim().equals(lineToRemove)) {
                            sb.append(line).append("\n");
                        }
                    }
                    try (BufferedWriter bw = new BufferedWriter(new FileWriter(fileName))) {
                        bw.write(sb.toString());
                    } catch (IOException e) {
                        System.err.println("Error writing to file: " + e.getMessage());
                    }
                } catch (IOException e) {
                    System.err.println("Error reading file: " + e.getMessage());
                }

                System.out.println("REMOVED! "+payment);
                DataPanel.remove(paymentPanel);
                invalidate();
                validate();
                repaint();
            }
        });

        gbc.gridx = 2;
        gbc.gridy = 1;
        paymentPanel.add(viewBtn, gbc);
        gbc.gridy = 2;
        paymentPanel.add(removeBtn, gbc);

        DataPanel.add(paymentPanel, BorderLayout.CENTER);
    }
    
