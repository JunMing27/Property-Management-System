    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String checkId = cpIdTxt1.getText();
        String guardId = guardIdTxt1.getText();
        String block = blockSpinner.getValue().toString();
        String date = dateTxt1.getText();
        String time = timeTxt1.getText();

        if(!block.equals("-----"))
        {
            errorMessage1.setText("");
            int dialog = JOptionPane.showConfirmDialog(null,
                "Record CANNOT be Changed After Saved ! Please Make Sure You Choose Right Block !", 
                "Confirmation", JOptionPane.YES_NO_OPTION);
            if(dialog == JOptionPane.YES_OPTION){
                ArrayList<String> dataList = new ArrayList<>();
                dataList.add(checkId);
                dataList.add(guardId);
                dataList.add(block);
                dataList.add(date);
                dataList.add(time);
                employee main = new employee();
                employee.employeeMethod innerMethod = main.new employeeMethod();
                main.setUserId(guardId);
                innerMethod.checkInCheckpoint("CheckpointRecord", dataList);
                this.dispose();
                guardCheckPointViewFrame view = new guardCheckPointViewFrame(idGet);
                view.setVisible(true);
            }
        }else{
            errorMessage1.setText("Block Number is Invalid !");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

