    private int pageLine=-1;
    
    private void setPagination(){
        pageLine=pageLine+1;
    }
    
    public void backButtonFunction(){
        pageLine = pageLine - 4;
        if (pageLine == -1){
            backPageBtn.setEnabled(false);
        }
    }
    
    private void displayData()
    {
        incident main = new incident();
        main.setUserId(idGet);
        setPagination();
        main.displayDataViewOwn(pageLine, "", "incident", "Incident");
        boolean boo = main.getStatus();
        if(boo==false){
            nextPageBtn.setEnabled(false);
        }
        if(main.getIncidentId() != null)
        {
            idTxt1.setText(main.getIncidentId());
            detailTxt1.setText(main.getIncidentDetail());
        }else{
            idTxt1.setText("no data");
            detailTxt1.setText("no data");
            editBtn1.setEnabled(false);
        }
        
        setPagination();
        main.displayDataViewOwn(pageLine, "", "incident", "Incident");
        boolean boo2 = main.getStatus();
        if(boo2==false){
            nextPageBtn.setEnabled(false);
        }
        if(main.getIncidentId() != null)
        {
            idTxt2.setText(main.getIncidentId());
            detailTxt2.setText(main.getIncidentDetail());
        }else{
            idTxt2.setText("no data");
            detailTxt2.setText("no data");
            editBtn2.setEnabled(false);
        }
    }
    
    
