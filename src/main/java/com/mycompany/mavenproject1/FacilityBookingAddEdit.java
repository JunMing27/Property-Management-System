private void editFile(String facId, String facName, String bookDate, String bookTime, String bookDuration)
    {
        //save into facility file
        try {
            String fileName = "FacilityBooking";
            File file = new File("src/main/java/com/mycompany/textFile/"+fileName+".txt");
            FileWriter fw = new FileWriter(file,true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(facId+","
                    +facName+","
                    +bookDate+","
                    +bookTime+","
                    +bookDuration+"\n");
            bw.close();
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ResidentMain.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ResidentMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private  ArrayList<ArrayList<String>> allUserDataInfo(String textFile) throws FileNotFoundException 
    {
        File file = new File(textFile);
        ArrayList<ArrayList<String>> allUserInfo = new ArrayList<>();
        if (file.exists()) {
            Scanner sc = new Scanner(file);
            String oneUserInfo; 
            String[] itemArray;
            ArrayList<String> itemArrayList;
            allUserInfo = new ArrayList<>();
            while (sc.hasNextLine()) { 
                oneUserInfo = sc.nextLine().trim(); 
                itemArray = oneUserInfo.split(","); 
                itemArrayList = new ArrayList<>(Arrays.asList(itemArray));
                allUserInfo.add(itemArrayList);
            }
        } 
        
        return allUserInfo;
    }
    
    private void removeFromFile(String fileName)
    {
        try {
            String filePath = "src/main/java/com/mycompany/textFile/"+fileName+".txt";
            ArrayList<ArrayList<String>> allUsers = allUserDataInfo(filePath);
            for(int j=0;j<allUsers.size();j++)
            {
                if(allUsers.get(j).get(3).equals(residentId)
                            && allUsers.get(j).get(2).equals(payToTxt1.getText())
                            && allUsers.get(j).get(3).equals(payAmountTxt1.getText())
                            && allUsers.get(j).get(4).equals(dueDateTxt1.getText()))
                {
                    allUsers.remove(j);
                    break;
                }
            }

            File file= new File(filePath);
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            for (int j=0; j<allUsers.size(); j++) 
            {
                ArrayList<String>item = allUsers.get(j);
                for(int k=0; k<item.size(); k++)
                {
                    if(k == item.size()-1)
                    {
                       bw.write(item.get(k));
                    }else{
                       bw.write(item.get(k)+",");
                    }
                }
                bw.write("\n");
            }
            bw.close();
            
        }catch (IOException ex) {
            Logger.getLogger(ResidentProfileEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }

