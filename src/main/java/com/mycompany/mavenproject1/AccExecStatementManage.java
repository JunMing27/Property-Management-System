    public void backButtonFunction(){
        PageLine = PageLine -6;
        if (PageLine==-1){
            BackAccExecInvoiceManage2.setEnabled(false);
        }
    }
    
    private Integer PageLine=-1;
    
    private String searchTxt ="";
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    
    public void setStatementData(){
        AccExecMain main = new AccExecMain();
        main.chooseTxtFile("InvoiceContent");
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Invoice");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecInvoiceManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            StatementID1.setText(main.getStatementID());
            UserID1.setText(main.getUserID());
            Username1.setText(main.getUsername());
            PaidDate1.setText(main.getPaidDate());
            PaidAmount1.setText(main.getPaidAmount());
            Description1.setText(main.getPayDescription());
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Invoice");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecInvoiceManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getInvoiceNumber() !=null){
                StatementID2.setText(main.getStatementID());
                UserID2.setText(main.getUserID());
                Username2.setText(main.getUsername());
                PaidDate2.setText(main.getPaidDate());
                PaidAmount2.setText(main.getPaidAmount());
                Description2.setText(main.getPayDescription());
            }else{
                StatementID2.setText("no data");
                UserID2.setText("no data");
                Username2.setText("no data");
                PaidDate2.setText("no data");
                PaidAmount2.setText("no data");
                Description2.setText("no data");
            }
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"Invoice");
                boolean boo = main.getStatus();
                if(boo==false){
                    NextAccExecInvoiceManage.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getInvoiceNumber() !=null){
                StatementID3.setText(main.getStatementID());
                UserID3.setText(main.getUserID());
                Username3.setText(main.getUsername());
                PaidDate3.setText(main.getPaidDate());
                PaidAmount3.setText(main.getPaidAmount());
                Description3.setText(main.getPayDescription());
            }else{
                StatementID3.setText("no data");
                UserID3.setText("no data");
                Username3.setText("no data");
                PaidDate3.setText("no data");
                PaidAmount3.setText("no data");
                Description3.setText("no data");
            }
    }
    
