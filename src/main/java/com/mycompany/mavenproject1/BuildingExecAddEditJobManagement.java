    private void UpdateAddJobManagementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateAddJobManagementActionPerformed
        // TODO add your handling code here:
          if(addEditDetector=="edit"){
            BuildingExecMain main = new BuildingExecMain();
            ArrayList<String> dataList = new ArrayList<String>();
            dataList.add(JobIDBuildingExec.getText());
            dataList.add(EmpNameBuildingExec.getText());
            dataList.add(EmpJobBuildingExec.getText());
            main.editOrAddData(dataList, "EmployeeJob","EmployeeJob.txt","edit");
        }else if(addEditDetector=="add"){
            BuildingExecMain main = new BuildingExecMain();
            ArrayList<String> dataList = new ArrayList<String>();
            main.getIncreasedID("InvoiceContent.txt","Invoice");
            dataList.add(main.getJobID());
            dataList.add(JobIDBuildingExec.getText());
            dataList.add(EmpNameBuildingExec.getText());
            dataList.add(EmpJobBuildingExec.getText());
            main.editOrAddData(dataList, "EmployeeJob","EmployeeJob.txt","add");
        }
    }//GEN-LAST:event_UpdateAddJobManagementActionPerformed

