    private Integer PageLine=-1;
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    private String searchTxt ="";
    
    public void backButtonToggle(){
        jButton6.setEnabled(false);
}
    
    public void backButtonFunction(){
        PageLine = PageLine -6;
        if (PageLine==-1){
            jButton6.setEnabled(false);
        }
    }
    
    public void setFacilityData(){
        AdminExecutiveMain main = new AdminExecutiveMain();
        main.chooseTxtFile("Facility Management");
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"facility");
                boolean boo = main.getStatus();
                if(boo==false){
                    jButton5.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            jLabel4.setText(main.getFacilityId());
            jLabel5.setText(main.getFacilityName());
            jLabel6.setText(main.getFacilityLocation());
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"facility");
                boolean boo = main.getStatus();
                if(boo==false){
                    jButton5.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getFacilityId() !=null){
                jLabel10.setText(main.getFacilityId());
                jLabel11.setText(main.getFacilityName());
                jLabel12.setText(main.getFacilityLocation());
            }else{
                jLabel10.setText("no data");
                jLabel11.setText("no data");
                jLabel12.setText("no data");
            }
            try {
                setPagination();
                main.displayDataView(PageLine,searchTxt,"facility");
                boolean boo = main.getStatus();
                if(boo==false){
                    jButton5.setEnabled(false);
                }

            } catch (IOException ex) {
                Logger.getLogger(BusManUserManageOption.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(main.getFacilityId() !=null){
                jLabel16.setText(main.getFacilityId());
                jLabel17.setText(main.getFacilityName());
                jLabel18.setText(main.getFacilityLocation());
            }else{
                jLabel16.setText("no data");
                jLabel17.setText("no data");
                jLabel18.setText("no data");
            }
    }
    
    //Method for delete button, so no need to be repetitive
    public void deleteBtn(String getText){
        if(!getText.equals("no data")){
            int ques = JOptionPane.showConfirmDialog(null,"confirm to delete this data", "Quit", JOptionPane.YES_NO_OPTION);
            if (ques == JOptionPane.YES_OPTION){
                AdminExecutiveMain main = new AdminExecutiveMain();
                main.chooseTxtFile("Facility Management");
                main.deleteFunction(getText);
                JOptionPane.showMessageDialog(null, "Deleted Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                AdminExecFacilityManage facility = new AdminExecFacilityManage();
                facility.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                facility.pack();
                facility.setResizable(false);
                facility.setLocationRelativeTo(null);
                facility.setVisible(true);
                facility.backButtonToggle();
                facility.setFacilityData();
            }
        }else{
            JOptionPane.showMessageDialog(null, "This is an empty data", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
