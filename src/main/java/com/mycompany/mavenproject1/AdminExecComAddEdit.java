    private String addEditDetector;
    
    
    public void addEditDetect(String functionType,String id) throws FileNotFoundException{
        if(functionType=="edit"){
            this.addEditDetector="edit";
            AdminExecutiveMain main = new AdminExecutiveMain();
            main.getDataViewSingle(id, "Complaint.txt","complaint");
            main.getDropDownData("ResidentProfile.txt","complaint");
            jButton3.setText("Update");
            jTextField1.setText(main.getComplaintId());
            jTextField2.setText(main.getComplaintDetail());
            jTextField3.setText(main.getComplaintReply());
            int i=0;
            for (String x : main.getDropDownData()) {
                jComboBox1.addItem((main.getDropDownData()).get(i));
                i++;
            }
            jComboBox1.setSelectedItem(main.getComplaintUserId());
        }else if (functionType=="add"){
            this.addEditDetector="add";
            AdminExecutiveMain main = new AdminExecutiveMain();
            jButton3.setText("Add");
            jTextField1.setVisible(false);jTextField3.setVisible(false);
            jLabel8.setVisible(false);jLabel11.setVisible(false);
            main.getDropDownData("ResidentProfile.txt","complaint");
            int i=0;
            for (String x : main.getDropDownData()) {
                jComboBox1.addItem((main.getDropDownData()).get(i));
                i++;
            }
        }
    }
    
