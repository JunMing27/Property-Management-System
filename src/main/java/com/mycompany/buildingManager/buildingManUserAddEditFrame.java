    private void AddEditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddEditBtnActionPerformed
        // TODO add your handling code here:
        if(addEditDetector=="edit"){
            if((userNameField.getText()).equals("") ||(userAgeField.getText()).equals("") ||(userPhoneField.getText()).equals("") || (credentialNameField.getText()).equals("") || (credentialPasswordField.getPassword()).equals("") || this.imageName==null) {
                JOptionPane.showMessageDialog(null, "Enter all field", "Warning", JOptionPane.ERROR_MESSAGE);
            }else{
                buildingManager main = new buildingManager();
                ArrayList<String> dataList = new ArrayList<String>();
                dataList.add(userIdField.getText());
                dataList.add(userNameField.getText());
                if(maleCheckBox.isSelected()){
                    dataList.add("male");
                }else{
                    dataList.add("female");
                }
                dataList.add(userAgeField.getText());
                dataList.add(userPhoneField.getText());
                dataList.add(imageName);
                dataList.add(credentialNameField.getText());
                char[] i = credentialPasswordField.getPassword();
                String stringPassword = new String(i);
                dataList.add(stringPassword);
                buildingManager.buildingManagerMethod mainInner = main.new buildingManagerMethod();
                mainInner.addEditUserExecutive(dataList, userType,fileType,"edit");
                //transfer uploaded image to our system image folder
                if (sourceFile!=null) {
                    File dest = new File("src/main/java/com/mycompany/image/" + this.imageName);
                    File source = sourceFile;
                    main.transferImage(source,dest);
                }
            }
        }else if(addEditDetector=="add"){
            if((userNameField.getText()).equals("") ||(userAgeField.getText()).equals("") ||(userPhoneField.getText()).equals("") || (credentialNameField.getText()).equals("") || (credentialPasswordField.getPassword()).equals("")|| this.imageName==null) {
                JOptionPane.showMessageDialog(null, "Enter all field", "Warning", JOptionPane.ERROR_MESSAGE);
            }else{
                buildingManager main = new buildingManager();
                ArrayList<String> dataList = new ArrayList<String>();
                main.getIncreasedID(fileType,userType);
                dataList.add(main.getUserId());
                dataList.add(userNameField.getText());
                if(maleCheckBox.isSelected()){
                    dataList.add("male");
                }else{
                    dataList.add("female");
                }
                dataList.add(userAgeField.getText());
                dataList.add(userPhoneField.getText());
                dataList.add(imageName);
                dataList.add(credentialNameField.getText());
                char[] i = credentialPasswordField.getPassword();
                String stringPassword = new String(i);
                dataList.add(stringPassword);
                System.out.println(dataList);
                main.setUserType(userType);
                buildingManager.buildingManagerMethod mainInner = main.new buildingManagerMethod();
                mainInner.addEditUserExecutive(dataList, userType,fileType,"add");
                //transfer uploaded image to our system image folder
                if (sourceFile!=null) {
                    File dest = new File("src/main/java/com/mycompany/image/" + this.imageName);
                    File source = sourceFile;
                    main.transferImage(source,dest);
                }
                //set value empty after inserting
                userIdField.setText("");
                userNameField.setText("");
                maleCheckBox.setSelected(false);
                femaleCheckBox.setSelected(false);
                userAgeField.setText("");
                userPhoneField.setText("");
                credentialNameField.setText("");
                credentialPasswordField.setText("");
                userImage.setIcon(null);
            }
        }
    }//GEN-LAST:event_AddEditBtnActionPerformed

    private void uploadImageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadImageBtnActionPerformed
        // TODO add your handling code here:
        try{
            // TODO add your handling code here:
            JFileChooser filechooser = new JFileChooser();
            buildingManager main = new buildingManager();
            filechooser.addChoosableFileFilter(main.new ImageFilter());
            filechooser.setAcceptAllFileFilterUsed(false);
            filechooser.showOpenDialog(null);
            sourceFile = filechooser.getSelectedFile();
            String filename = sourceFile.getName();
            this.imageName=filename;
            // get image path first so that the staff can add the image into this system's folder after clicking add item button:
            imagePath[0] = sourceFile.getAbsolutePath();
            try{
                // resize the image so that it can fit the border:
                BufferedImage ItemImage = ImageIO.read(new File(imagePath[0]));
                Image resizedImage = ItemImage.getScaledInstance(165, 130, Image.SCALE_SMOOTH);
                userImage.setIcon(new ImageIcon(resizedImage));
            }
            catch(IOException e){}
        }
        catch(Exception e){
        }
    }//GEN-LAST:event_uploadImageBtnActionPerformed

