    private Integer PageLine=-1;
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    private String searchTxt ="";
    private String userType="";
    
    public void setUserType(String type){
        this.userType=type;
    }
    
    public void backButtonToggle(){
        previousPage.setEnabled(false);
}
    
    public void backButtonFunction(){
        PageLine = PageLine -4;
        if (PageLine==-1){
            previousPage.setEnabled(false);
        }
    }
    
    public void setEmployeeTeamData(String employeeTeam){
        employee employeeMain = new employee();
        setPagination();
        employeeMain.displayDataView(PageLine,employeeTeam,"Employee");
        boolean boo = employeeMain.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(employeeMain.getUserId() !=null){
            EmployeeIdLabel1.setText(employeeMain.getUserId());
            EmployeeNameLabel1.setText(employeeMain.getUserName());
            EmployeeJobLabel1.setText(employeeMain.getEmployeeJobScope());
        }else{
            EmployeeIdLabel1.setText("no data");
            EmployeeNameLabel1.setText("no data");
            EmployeeJobLabel1.setText("no data");
        }
        setPagination();
        employeeMain.displayDataView(PageLine,employeeTeam,"Employee");
        boo = employeeMain.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(employeeMain.getUserId() !=null){
            EmployeeIdLabel2.setText(employeeMain.getUserId());
            EmployeeNameLabel2.setText(employeeMain.getUserName());
            EmployeeJobLabel2.setText(employeeMain.getEmployeeJobScope());
        }else{
            EmployeeIdLabel2.setText("no data");
            EmployeeNameLabel2.setText("no data");
            EmployeeJobLabel2.setText("no data");
        }
        setPagination();
        employeeMain.displayDataView(PageLine,employeeTeam,"Employee");
        boo = employeeMain.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(employeeMain.getUserId() !=null){
            EmployeeIdLabel3.setText(employeeMain.getUserId());
            EmployeeNameLabel3.setText(employeeMain.getUserName());
            EmployeeJobLabel3.setText(employeeMain.getEmployeeJobScope());
        }else{
            EmployeeIdLabel3.setText("no data");
            EmployeeNameLabel3.setText("no data");
            EmployeeJobLabel3.setText("no data");
        }
    }
    
    
    
    
    
    
