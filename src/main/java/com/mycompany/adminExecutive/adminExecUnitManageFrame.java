    private Integer PageLine=-1;
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    private String searchTxt ="";
    
    public void backButtonToggle(){
        previousPage.setEnabled(false);
}
    
    public void backButtonFunction(){
        PageLine = PageLine -6;
        if (PageLine==-1){
            previousPage.setEnabled(false);
        }
    }
    

    public void setUnitData(){
        unit unitClass = new unit();
        setPagination();
        unitClass.displayDataView(PageLine,searchTxt,"Unit");
        boolean boo = unitClass.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(unitClass.getUnitId() !=null){
            unitIdLabel1.setText(unitClass.getUnitId());
            unitNameLabel1.setText(unitClass.getUnitNumber());
            unitStatusLabel1.setText(unitClass.getUnitStatus());
        }else{
            unitIdLabel1.setText("no data");
            unitNameLabel1.setText("no data");
            unitStatusLabel1.setText("no data");
        }
        setPagination();
        unitClass.displayDataView(PageLine,searchTxt,"Unit");
        boo = unitClass.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(unitClass.getUnitId() !=null){
            unitIdLabel2.setText(unitClass.getUnitId());
            unitNameLabel2.setText(unitClass.getUnitNumber());
            unitStatusLabel2.setText(unitClass.getUnitStatus());
        }else{
            unitIdLabel2.setText("no data");
            unitNameLabel2.setText("no data");
            unitStatusLabel2.setText("no data");
        }
        setPagination();
        unitClass.displayDataView(PageLine,searchTxt,"Unit");
        boo = unitClass.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(unitClass.getUnitId() !=null){
            unitIdLabel3.setText(unitClass.getUnitId());
            unitNameLabel3.setText(unitClass.getUnitNumber());
            unitStatusLabel3.setText(unitClass.getUnitStatus());
        }else{
            unitIdLabel3.setText("no data");
            unitNameLabel3.setText("no data");
            unitStatusLabel3.setText("no data");
        }
    }
    
    //Method for delete button, so no need to be repetitive
    public void deleteBtn(String getText){
        if(!getText.equals("no data")){
            int ques = JOptionPane.showConfirmDialog(null,"confirm to delete this data", "Quit", JOptionPane.YES_NO_OPTION);
            if (ques == JOptionPane.YES_OPTION){
                adminExecutive main = new adminExecutive();
                main.chooseTxtFile("Unit");
                adminExecutive.adminExecutiveMethod mainInner = main.new adminExecutiveMethod();
                mainInner.deleteUnit(getText);
                JOptionPane.showMessageDialog(null, "Deleted Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
                adminExecUnitManageFrame unitManage = new adminExecUnitManageFrame();
                unitManage.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                unitManage.pack();
                unitManage.setResizable(false);
                unitManage.setLocationRelativeTo(null);
                unitManage.setVisible(true);
                unitManage.backButtonToggle();
                unitManage.setUnitData();
            }
        }else{
            JOptionPane.showMessageDialog(null, "This is an empty data", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }
    
