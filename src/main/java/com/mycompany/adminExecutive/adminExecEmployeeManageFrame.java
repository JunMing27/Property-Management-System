    private Integer PageLine=-1;
    
    public void setPagination(){
        PageLine=PageLine+1;
    }
    
    private String searchTxt ="";
    private String userType="";
    
    public void setUserType(String type){
        this.userType=type;
    }
    
    public void backButtonToggle(){
        previousPage.setEnabled(false);
}
    
    public void backButtonFunction(){
        PageLine = PageLine -4;
        if (PageLine==-1){
            previousPage.setEnabled(false);
        }
    }
    
    public void setEmployeeData() throws IOException{
        employee main = new employee();
        main.chooseTxtFile(userType);
        
        setPagination();
        main.displayDataView(PageLine,searchTxt,userType);
        boolean boo = main.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(main.getUserId() !=null){
            employeeId1.setText(main.getUserId());
            employeeName1.setText(main.getUserName());
            try{
                BufferedImage UserImage = ImageIO.read(new File("src/main/java/com/mycompany/image/"+main.getUserImage()));
                Image resizedImage = UserImage.getScaledInstance(120, 100, Image.SCALE_SMOOTH);
                employeeImage1.setIcon(new ImageIcon(resizedImage));
                }catch(Exception e){}
        }else{
            employeeId1.setText("no data");
            employeeName1.setText("no data");
            employeeImage1.setIcon(null);
        }
        setPagination();
        main.displayDataView(PageLine,searchTxt,userType);
        boo = main.getStatus();
        if(boo==false){
            nextPage.setEnabled(false);
        }
        if(main.getUserId() !=null){
            employeeId2.setText(main.getUserId());
            employeeName2.setText(main.getUserName());
            try{
                BufferedImage UserImage = ImageIO.read(new File("src/main/java/com/mycompany/image/"+main.getUserImage()));
                Image resizedImage = UserImage.getScaledInstance(120, 100, Image.SCALE_SMOOTH);
                employeeImage2.setIcon(new ImageIcon(resizedImage));
                }catch(Exception e){}
        }else{
            employeeId2.setText("no data");
            employeeName2.setText("no data");
            employeeImage2.setIcon(null);
        }
    
    }
    
    
    //Method for delete button, so no need to be repetitive
    public void deleteBtn(String getText){
        if(!getText.equals("no data")){
            int ques = JOptionPane.showConfirmDialog(null,"confirm to delete this data", "Quit", JOptionPane.YES_NO_OPTION);
            if (ques == JOptionPane.YES_OPTION){
                adminExecutive main = new adminExecutive();
                main.chooseTxtFile("Employee");
                adminExecutive.adminExecutiveMethod mainInner = main.new adminExecutiveMethod();
                mainInner.deleteUserExecutive(getText);
                JOptionPane.showMessageDialog(null, "Deleted Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
                adminExecEmployeeManageFrame EmployeeManage = new adminExecEmployeeManageFrame();
                EmployeeManage.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                EmployeeManage.pack();
                EmployeeManage.setResizable(false);
                EmployeeManage.setLocationRelativeTo(null);
                EmployeeManage.setVisible(true);
                EmployeeManage.backButtonToggle();
                try {
                    EmployeeManage.setUserType("Employee");
                    EmployeeManage.setEmployeeData();
                } catch (IOException ex) {

                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "This is an empty data", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
